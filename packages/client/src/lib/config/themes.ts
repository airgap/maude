/** IDs of all immersive themes (used to determine visual style for data-hypertheme) */
const IMMERSIVE_IDS = new Set(THEMES.filter((t) => t.category === 'immersive').map((t) => t.id));

/** Find a theme config by ID */
export function findTheme(id: string): ThemeConfig | undefined {
  return THEMES.find((t) => t.id === id);
}

/** Get the default theme */
export function getDefaultTheme(): ThemeConfig {
  return THEMES[0]; // dark
}

/**
 * Get the visual style ID for a given theme ID.
 * Standard themes use 'tech' (the default structural style).
 * Immersive themes use their own ID as the visual style.
 * This maps to the `data-hypertheme` attribute on <html>.
 */
export function getVisualStyle(themeId: string): string {
  return IMMERSIVE_IDS.has(themeId) ? themeId : 'tech';
}

/**
 * Check whether a theme is immersive (has its own visual style + colors).
 */
export function isImmersiveTheme(themeId: string): boolean {
  return IMMERSIVE_IDS.has(themeId);
}

