/**
 * Unified Theme System
 *
 * Each theme is a complete visual package: colors, shapes, motion, effects.
 *
 * Standard themes (Dark, Monokai, etc.) use the Tech visual style with
 * colors from CSS [data-theme] selectors. Their structural vars match Tech.
 *
 * Immersive themes (Arcane, Ethereal, Study, etc.) bring their own
 * complete color palette AND unique structural styling, ambient effects,
 * suggested fonts, and optional commentary personality.
 */

export interface ThemeConfig {
  id: string;
  label: string;
  description: string;
  /** SVG path data for the icon (rendered in a 24×24 viewBox). Immersive themes only. */
  icon?: string;
  /** 'standard' = colors from CSS, Tech shapes. 'immersive' = full custom experience. */
  category: 'standard' | 'immersive';
  /** Base type for CSS fallback: 'dark' or 'light' */
  type: 'dark' | 'light';
  /** Shape / motion / text CSS vars (--ht-*) */
  cssVars: Record<string, string>;
  /**
   * Full color palette override. Applied as inline styles on <html>,
   * overriding whatever [data-theme] sets. Only used by immersive themes.
   */
  colorOverrides?: Record<string, string>;
  suggestedMonoFont: string;
  suggestedSansFont: string;
  /**
   * Optional suggested commentary personality for this theme.
   * When the user switches to this theme, the commentary panel
   * can auto-activate this personality for a more immersive experience.
   */
  suggestedPersonality?: string;
}

